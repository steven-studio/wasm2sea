  TEST=$(echo $entry | cut -d' ' -f1)
  ARGS=$(echo $entry | cut -d' ' -f2-)

  if [ "$TEST" = "jacobi1d_kernel" ]; then
    bash ~/wasm2sea/tests/run_test.sh jacobi1d_kernel > /tmp/actual_out.txt 2>&1

    if grep -q "PASS jacobi1d differential test" /tmp/actual_out.txt; then
      echo "PASS: jacobi1d_kernel"
      PASS=$((PASS+1))
    else
      echo "FAIL: jacobi1d_kernel"
      cat /tmp/actual_out.txt
      FAIL=$((FAIL+1))
    fi
    continue
  fi

  if [ "$TEST" = "minidp" ]; then
    bash ~/wasm2sea/tests/run_minidp_test.sh minidp kernel_minidp > /tmp/actual_out.txt 2>&1

    if grep -q "PASS minidp differential test" /tmp/actual_out.txt; then
      echo "PASS: minidp"
      PASS=$((PASS+1))
    else
      echo "FAIL: minidp"
      cat /tmp/actual_out.txt
      FAIL=$((FAIL+1))
    fi
    continue
  fi

  # wasmtime expected
  EXPECTED=$(wasmtime run --invoke test ~/wasm2sea/tests/${TEST}.wasm $ARGS 2>/dev/null)

  # your pipeline actual
  bash ~/wasm2sea/tests/run_test.sh $TEST $ARGS > /tmp/actual_out.txt 2>&1
  ACTUAL=$(bash ~/wasm2sea/tests/run_test.sh $TEST $ARGS 2>/dev/null | tail -1)

  if [ "$EXPECTED" = "$ACTUAL" ]; then
    echo "PASS: $TEST ($ARGS) = $EXPECTED"
    PASS=$((PASS+1))
  else
    echo "FAIL: $TEST ($ARGS) expected=$EXPECTED actual=$ACTUAL"
    FAIL=$((FAIL+1))
  fi
done

echo ""
